#Comparison operators

#equal
print(2==2)
#greater
print(2>1)
#equal or greater
print(22>=21)
#equal or less
print(30<=5)
print(5<=30)
#not equal
print(5!=5)

#Chain comparison operators
a = 1
b = 2
c = 3
print(a==1 and b==2)
print(a==1 and b==3)
print(a==1 or b==1)
print(a!=b or a==c)
print(a==b or b==c)
